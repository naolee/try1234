    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {   
        
        String std_id = request.getParameter("std_id");
        String std_name = request.getParameter("std_name");
        String exmr1_name = request.getParameter("exmr1_name");
        String exmr2_name = request.getParameter("exmr2_name");
        String date = request.getParameter("date");
        String time = request.getParameter("time");
        
        HttpSession session=request.getSession(); 
        session.setAttribute("std_id",std_id);
        session.setAttribute("std_name",std_name);
        session.setAttribute("exmr1_name",exmr1_name);
        session.setAttribute("exmr2_name",exmr2_name);
        session.setAttribute("date",date);
        session.setAttribute("time",time);
        
        response.sendRedirect("SelectAppointmentView.jsp");
    }

